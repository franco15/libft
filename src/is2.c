#include "libft.h"

int	ft_isalnum(int c)
{
	return ((c >= 'A' && c <= 'Z') || (c >= 'a' && c <= 'z') ||
			(c >= '0' && c <= '9'));
}

int	ft_isalpha(int c)
{
	return ((c >= 'A' && c <= 'Z') || (c >= 'a' && c <= 'z'));
}

int	ft_isascii(int c)
{
	return ((c >= 0 && c <= 127) || (c >= 000 && c <= 0177));
}

int	ft_isblank(int c)
{
	return (c == 32 || c == 9);
}

int	ft_isprint(int c)
{
	return (c >= ' ' && c <= '~');
}
